				$data[] = $row;
			}
			return $data;
		}
	}

	function insert()
	{
		if(isset($_POST["name"]))
		{
			$form_data = array(
				':name'		=>	$_POST["name"],
			);
			$query = "
			INSERT INTO users 
			(name) VALUES 
			(:name)
			";
			$statement = $this->connect->prepare($query);
